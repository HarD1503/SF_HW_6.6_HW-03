post1_title = 'Этот гул неспроста: как работает выжимной подшипник сцепления и почему он выходит из строя'
post1_text = '''
До сих пор можно встретить тех, кто убеждён, что автоматическая коробка передач может 
доставить больше неприятностей, чем ручная. На самом деле, это не совсем так, и среди 
ручных коробок тоже встречаются не самые удачные конструкции. А среди автоматов – 
удивительно живучие гидротрансформаторные коробки. Кроме того, сегодня с ручными коробками 
часто ставят двухмассовые маховики, которые имеют ограниченный ресурс и стоят, как небольшой 
ремонт старого автомата. Одним словом, с механикой тоже всегда найдётся, куда вложить деньги. 
Особенно если помнить про сцепление, в котором тоже есть чему развалиться. Например, выжимному подшипнику.

Два диска и подшипник  
Зачем вообще в сцеплении нужен подшипник, да ещё и выжимной? Ответ лежит на поверхности: 
для того, чтобы это сцепление выжимать. Вспомним, как оно работает.В составе сцепления 
есть два диска: ведущий (он же «корзина») и ведомый. Кожух ведущего диска крепится к маховику. 
Внутри него на первичном валу коробки передач установлен ведомый диск с фрикционными накладками 
с обеих сторон. Ведомый диск при включенном сцеплении плотно прижат к маховику нажимным диском 
корзины (то есть, ведущего диска) сцепления. Таким образом крутящий момент через ведущий и ведомые 
диски передаётся на вал коробки. Однако при переключении передач требуется разомкнуть передачу момента,
и для этого водитель выключает сцепление.
Выключение сцепления – это размыкание связки ведущего и ведомого дисков. Происходит оно следующим образом.
 Когда водитель нажимает на педаль сцепления, вилка передвигает выжимной подшипник по валу коробки 
 (либо тросиком, либо с помощью гидравлического привода). В целом, единственное отличие между 
 выжимными подшипниками и заключается в реализации их приводов. Самая примитивная схема 
 (сейчас уже устаревшая) – механическая, с тросовым приводом от педали сцепления. В этом случае тросик 
 тянет вилку, которая перемещает по валу подшипник. В более современных автомобилях привод обычно 
 гидравлический (в котором вилки сцепления нет вообще, зато есть гидравлическая муфта подшипника). 
 Этот тип заметно сложнее конструктивно, но ездить с ним проще: усилие на педали меньше, его легче 
 дозировать, а работает выжим плавнее, чем с тросовым приводом. 
'''

